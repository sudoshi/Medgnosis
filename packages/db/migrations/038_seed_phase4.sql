-- ─── Synthetic future appointments (enables AMP-1 pre-visit) ─────────────────
-- ~250 problem-list patients get a Scheduled visit 1..21 days out, with their PCP.
INSERT INTO phm_edw.appointment
  (patient_id, provider_id, appointment_date, start_time, end_time, appointment_type, status)
SELECT
  c.patient_id,
  c.pcp_provider_id,
  CURRENT_DATE + (((c.rn % 21) + 1) || ' days')::interval,
  TIME '09:00',
  TIME '09:20',
  'office_visit',
  'Scheduled'
FROM (
  SELECT p.patient_id, p.pcp_provider_id,
         row_number() OVER (ORDER BY p.patient_id) AS rn
  FROM phm_edw.patient p
  WHERE p.active_ind = 'Y' AND p.pcp_provider_id IS NOT NULL
    AND EXISTS (
      SELECT 1 FROM phm_edw.problem_list pl
      WHERE pl.patient_id = p.patient_id AND pl.active_ind = 'Y' AND pl.problem_status = 'Active'
    )
  ORDER BY p.patient_id
  LIMIT 250
) c;

