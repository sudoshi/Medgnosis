-- Link recurring lab items by LOINC (one item per code); skips codes with no item.
INSERT INTO phm_edw.order_protocol_item (protocol_id, item_id, interval_days)
SELECT pr.protocol_id, picks.item_id, picks.interval_days
FROM phm_edw.order_protocol pr
CROSS JOIN LATERAL (
  SELECT item_id, interval_days FROM (
    SELECT (SELECT item_id FROM phm_edw.order_set_item WHERE loinc_code = '4548-4'  AND active_ind='Y' ORDER BY item_id LIMIT 1) AS item_id, 180 AS interval_days
    UNION ALL
    SELECT (SELECT item_id FROM phm_edw.order_set_item WHERE loinc_code = '24331-1' AND active_ind='Y' ORDER BY item_id LIMIT 1), 365
    UNION ALL
    SELECT (SELECT item_id FROM phm_edw.order_set_item WHERE loinc_code = '9318-7'  AND active_ind='Y' ORDER BY item_id LIMIT 1), 365
    UNION ALL
    SELECT (SELECT item_id FROM phm_edw.order_set_item WHERE loinc_code = '51990-0' AND active_ind='Y' ORDER BY item_id LIMIT 1), 365
  ) x WHERE item_id IS NOT NULL
) picks
WHERE pr.protocol_code = 'CHRONIC_MONITORING'
ON CONFLICT (protocol_id, item_id) DO NOTHING;
